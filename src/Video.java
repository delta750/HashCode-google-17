
public class Video {

	int id;
	int size;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getSize() {
		return size;
	}
	public void setSize(int size) {
		this.size = size;
	}
	public Video(int id, int size) {
		this.id = id;
		this.size = size;
	}
	
	
}
